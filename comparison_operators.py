#Equal to

value = 8
value2  = 8
# Equal to uses  == (double equals to)
print(value == value2)

#Not Equal to
# Not equal uses !=

print(value2 != value)

#Greater than >
print(value > value2)

#Lesser than <
print(value < value2)

#Lesser or Equal to <=
print(value2 <= value)

#Grater than or equal to >=
print(value2 >= value)